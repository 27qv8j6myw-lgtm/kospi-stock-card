/** 캔들질 카드·툴팁용 CLV5/CLV10 파싱 (예: "CLV5 -0.12 / CLV10 -0.25") */
export function parseClvFromMetricValue(value: string): {
  clv5: number | null
  clv10: number | null
} {
  const m5 = value.match(/CLV5\s*([+-]?\d+(?:\.\d+)?)/i)
  const m10 = value.match(/CLV10\s*([+-]?\d+(?:\.\d+)?)/i)
  const n5 = m5 ? Number(m5[1]) : NaN
  const n10 = m10 ? Number(m10[1]) : NaN
  return {
    clv5: Number.isFinite(n5) ? n5 : null,
    clv10: Number.isFinite(n10) ? n10 : null,
  }
}

export function formatClvSigned(n: number): string {
  return `${n > 0 ? '+' : ''}${n}`
}

